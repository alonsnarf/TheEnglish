package com.alonsnarf.theenglish.object;

public class Setting
{
  private int id;
  private String email;
  private int mainWord;
  private int time;

  public int getId()
  {
    return id;
  }

  public void setId(int id)
  {
    this.id = id;
  }

  public String getEmail()
  {
    return email;
  }

  public void setEmail(String email)
  {
    this.email = email;
  }

  public int getMainWord()
  {
    return mainWord;
  }

  public void setMainWord(int mainWord)
  {
    this.mainWord = mainWord;
  }

  public int getTime()
  {
    return time;
  }

  public void setTime(int time)
  {
    this.time = time;
  }
}
